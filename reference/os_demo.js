const os = require('os');

//platform
console.log(os.platform());

//CPU architecture
console.log(os.arch());

//CPU core info
console.log(os.cpus());

//Free memory
console.log(os.freemem());

//total memory
console.log(os.totalmem());

//Home dir
console.log(os.homedir());


//Uptime of the pc
console.log(os.uptime());
